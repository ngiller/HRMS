/**
 * Frontend RBAC Permission Helper
 *
 * Membaca user data dari localStorage (disimpan oleh auth.saveSession())
 * dan menyediakan function untuk mengecek permission module + action.
 *
 * Usage:
 *   import { hasPermission } from '$lib/permissions.js';
 *
 *   {#if hasPermission('employee', 'create')}
 *     <button>Tambah Karyawan</button>
 *   {/if}
 */

import config from '$lib/config.js';

interface UserPermissions {
  [module: string]: {
    [action: string]: boolean;
  };
}

interface UserData {
  id: string;
  employee_id: string;
  full_name: string;
  email: string;
  role_slug: string;
  role_name: string;
  permissions?: UserPermissions;
}

/**
 * Mendapatkan user data dari localStorage.
 * Mengembalikan null jika tidak ada session.
 */
export function getUser(): UserData | null {
  try {
    const data =
      typeof localStorage !== 'undefined'
        ? localStorage.getItem(config.USER_DATA_KEY)
        : null;
    return data ? JSON.parse(data) : null;
  } catch {
    return null;
  }
}

/**
 * Mengecek apakah user yang sedang login memiliki akses ke module + action tertentu.
 *
 * @param module - Nama modul (contoh: 'employee', 'department', 'user_management')
 * @param action - Nama aksi (contoh: 'create', 'read', 'update', 'delete')
 * @returns true jika user memiliki akses, false jika tidak
 *
 * Contoh:
 *   hasPermission('employee', 'create')  // bisa buat karyawan?
 *   hasPermission('department', 'delete') // bisa hapus departemen?
 */
export function hasPermission(module: string, action: string): boolean {
  const user = getUser();
  if (!user) return false;
	// Jika data permissions belum tersedia (session lama sebelum fitur RBAC),
	// default ke false — deny by default demi keamanan data sensitif.
	// User perlu re-login untuk mendapatkan permissions dari backend.
	if (!user.permissions) return false;

  const modulePerms = user.permissions[module];
  if (!modulePerms) return false;

  return modulePerms[action] === true;
}

/**
 * Menentukan apakah user bisa mengakses modul tertentu secara keseluruhan (minimal read).
 * Berguna untuk menyembunyikan menu sidebar.
 */
export function canAccess(module: string): boolean {
  return hasPermission(module, 'read');
}

